          <h3>SOBRE</h3>
            <p>
            O IF Baiano (Instituto Federal de Educação, Ciência e Tecnologia Baiano) é uma instituição 
            de ensino pública federal no estado da Bahia. Ele é voltado para a educação técnica, tecnológica e 
            superior, com ênfase em ciências agrárias e áreas relacionadas ao desenvolvimento rural sustentável, mas também 
            abrange outras áreas de formação.</p>


              

              <p> CAMPUS
O IF Baiano possui diversos campus distribuídos por todo o estado da Bahia, como:

Alagoinhas
Bom Jesus da Lapa
Catu
Guanambi
Itapetinga
Santa Inês
Senhor do Bonfim
Serrinha, entre outros.
Cada campus atende às demandas específicas da região, oferecendo cursos alinhados com as características econômicas e sociais locais.<br><br>

 CURSOS OFERECIDOS
O IF Baiano disponibiliza educação em diferentes níveis:

Ensino Médio Técnico (Integrado e Subsequente):
Agropecuária
Agroecologia
Zootecnia
Administração
Informática
Meio Ambiente
Ensino Superior:
Agronomia
Medicina Veterinária
Ciências da Computação
Engenharia de Pesca
Licenciaturas em Biologia, Física e outras áreas.
Pós-Graduação:
Lato sensu e stricto sensu, com foco em temas como Desenvolvimento Rural e Sustentabilidade.<br><br>
 ÁREAS DE ATUAÇÃO E MISSÃO
O IF Baiano é reconhecido por seu compromisso com o desenvolvimento rural e sustentável. Ele investe em:
Pesquisa aplicada à agricultura e pecuária.
Projetos de extensão voltados para comunidades locais.
Formação de profissionais preparados para atuar no campo e em áreas correlatas.</p>

              
